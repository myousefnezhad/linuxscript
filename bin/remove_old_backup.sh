# Functions
function write_log() {
	printf "%-50s[%-22s]\n" $1 $(date +%Y/%m/%d_%a_%H:%M:%S) >> "$LOG_PATH.txt"
}
# Start Backup
write_log "IPCAM::REMOVING::START"
find $CAM_ROOT -type d -mtime +7 > $CAM_TEMP
cat $CAM_TEMP >> "$LOG_PATH.txt"
cat $CAM_TEMP | xargs rm -rf
write_log "IPCAM::REMOVING::FINISHED"
# Stop Backup
